 L’acronyme FIRST désigne les 5 qualités fondamentales d’un bon test :
 - Fast    Les tests doivent s’exécuter rapidement
 - Isolated    Chaque test doit être indépendant des autres.
 - Repeatable    Le test doit produire le même résultat à chaque exécution.
 - Self-validating    Le test doit réussir ou échouer sans nécessiter d’analyse humaine.
 - Timely    Les tests doivent être écrits au bon moment : en TDD, avant le code de production.
